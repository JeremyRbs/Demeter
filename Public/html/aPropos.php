        <p style="text-align:justify"><strong> </strong><br />
        C’est en 2021 à Lyon que Salah Dardouri rencontre le Demeter Tacos. Cette galette garnie de viande, de frites et de sauce fromagère onctueuse le séduit et lui donne l’envie de la partager.
Rapidement il décide de créer l’enseigne «Le tacos de Chalon» à Chalon-Sur-Saône où il personnalise la recette et la rend célèbre. Le tacos à 1, 2 ou 3 viandes voit le jour, avec des recettes mêlant cuisine traditionnelle et restauration rapide. Le tacos s’impose sur le marché du fast-food comme un sandwich pratique à déguster, plein de saveurs, riche en goût, rassasiant et peu couteux.</p>

